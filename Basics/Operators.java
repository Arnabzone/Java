/*
 * 
 * Operators are special symbols used to perform operations on variables and
 * values. They are the "verbs" of your code that tell the computer to add,
 * compare, or modify data.
 * 
 * Java operators are grouped based on the functionality they provide.
 * 
 * 
 * 1. Arithmetic Operators
 * Used to perform basic mathematical operations.
 * 
 * Operator,Name,Description,"Example (a=10,b=3)"
 * +| Addition| Adds two values |a+b=13
 * -,Subtraction,Subtracts one from another,a−b=7
 * ,Multiplication,Multiplies two values,a∗b=30
 * /,Division,Divides one by another,a/b=3 (integer division)
 * %,Modulo,Returns the remainder,a%b=1
 * 
 * Note: Integer division 10 / 3 results in 3 because Java drops the decimal. To
 * get 3.33, at least one number must be a double.
 * 
 * 2. Assignment Operators
 * Used to assign values to variables. You’ve already seen =, but there are
 * "compound" versions that combine math and assignment.int x = 10;x += 5; (Same
 * as $x = x + 5$; result is 15)x -= 2; (Same as $x = x - 2$; result is 13)
 * 
 * 3. Comparison (Relational) Operators
 * Used to compare two values. These always return a boolean (true or false).
 * 
 * == : Equal to (e.g., 5 == 5 is true)
 * 
 * != : Not equal to
 * 
 * > : Greater than
 * 
 * < : Less than
 * 
 * >= : Greater than or equal to
 * 
 * 4. Logical Operators
 * Used to determine the logic between variables or values, typically to combine
 * multiple comparisons.
 * 
 * Operator,Name,Description,Example
 * &&,Logical AND,Returns true if both sides are true,(5 > 3 && 8 > 5) → true
 * `,,`,Logical OR
 * !,Logical NOT,Reverses the result (true → false),!(5 > 3) → false
 * 
 * 5. Unary Operators: Increment and DecrementThese are used to increase or
 * decrease a value by 1.++x or x++ (Increments $x$ by 1)--x or x-- (Decrements
 * $x$ by 1)
 * 
 */